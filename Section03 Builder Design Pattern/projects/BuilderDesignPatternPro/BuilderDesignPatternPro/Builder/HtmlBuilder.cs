using BuilderDesignPatternPro.Models;

namespace BuilderDesignPatternPro.Builder
{
    //we define HtmlBuilder that using HtmlELement class that provide creational pattern that create String builder internally 
    //this HtmlBuilder provide add child and tostring custom method
    public class HtmlBuilder
    {
        private readonly string rootName;
        HtmlElement root = new HtmlElement();

        public HtmlBuilder(string rootName)
        {
            this.rootName = rootName;
            root.Name = rootName;
        }

        public void AddChild(string childName, string childText)
        {
            var e = new HtmlElement(childName, childText);
            root.Elements.Add(e);
        }

        public override string ToString()
        {
            return root.ToString();
        }

        public void Clear()
        {
            root = new HtmlElement { Name = rootName };
        }
    }
}
